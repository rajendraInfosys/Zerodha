package Logical_Program;

public class Print_ASCII_Number {
public static void main(String[] args) {
	char ch1='a';
	char ch2='b';
	char ch3='c';
	char ch4='d';
	
	int num1=ch1;
	int num2=ch2;
	int num3=ch3;
	int num4=ch4;
	
	System.out.println(num1);
	System.out.println(num2);
	System.out.println(num3);
	System.out.println(num4);
}
}
