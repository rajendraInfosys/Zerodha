package Method1;

public class Static_call_from_same_class {
	public static void main(String[] args) {
		m1();
		m2();
		m3();
	}
	
	public static void m1() {
		System.out.println("I am Rajendra Yamgar");
	}
	
	public static void m2() {
		System.out.println("I am BE Mechanical graduate");
	}
	
	public static void m3() {
		System.out.println("I am working as QA engineer");
	}
	
}
