package Conditional_statements;

public class Switch {
public static void main(String[] args) {
	switch("Rajendra") {
	
	case("Sonali"):{
		System.out.println("Beautiful");
		break;
	}
	case("Shravi"):{
		System.out.println("Pillu");
		break;
	}
	case("Rajendra"):{
		System.out.println("Talented");
		break;
	}
	case("Suraj"):{
		System.out.println("Hard Working");
		break;
	}
	
	}
}
}
