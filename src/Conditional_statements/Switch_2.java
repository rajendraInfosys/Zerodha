package Conditional_statements;

public class Switch_2 {
	public static void main(String[] args) {
		switch ("Sunday is Holiday") {
		
		case("Monday is Holiday"):{
			System.out.println("Euuu");
		break;
		}
		
		case("Thursday is Holiday"):{
			System.out.println("Euuuuuuuu");
		break;
		}
		
		case("Sunday is Holiday"):{
			System.out.println("euuuuuuuuuuuuuu");
			break;
		}
		
		
	}
	}
}
