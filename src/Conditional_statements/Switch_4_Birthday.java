package Conditional_statements;

public class Switch_4_Birthday {
	public static void main(String[] args) {
		switch ("8 April") {
		
		case("1 January"):{
			System.out.println("Suraj's birthday");
		break;
		}
		
		case("15 March"):{
			System.out.println("Sushant's Birthday");
		break;
		}
		
		case("8 April"):{
			System.out.println("My Birthday");
			break;
		}
	}
	}
}
